package com.fawri.b3g.id.doc.result;

public class Result {
    private String key;
    private String value;


    public Result(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
